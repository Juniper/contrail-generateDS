    def _build_heat_properties(self):
        for prop_name in self.cls.prop_fields:
            if prop_name in self.skip_list:
                continue
            prop_is_simple = self.cls.prop_field_types[prop_name][0]
            prop_type = self.cls.prop_field_types[prop_name][1]
